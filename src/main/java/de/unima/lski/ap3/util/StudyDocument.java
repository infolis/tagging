package de.unima.lski.ap3.util;

public class StudyDocument {
	private String abstractGerman=null;
	private String abstractEnglish=null;
	private String wglIdentifier=null;
	private String Identifier=null;
	
	public String getAbstractGerman() {
		return abstractGerman;
	}
	public void setAbstractGerman(String abstractGerman) {
		this.abstractGerman = abstractGerman;
	}
	public String getAbstractEnglish() {
		return abstractEnglish;
	}
	public void setAbstractEnglish(String abstractEnglish) {
		this.abstractEnglish = abstractEnglish;
	}
	public String getWglIdentifier() {
		return wglIdentifier;
	}
	public void setWglIdentifier(String wglIdentifier) {
		this.wglIdentifier = wglIdentifier;
	}
	public String getIdentifier() {
		return Identifier;
	}
	public void setIdentifier(String identifier) {
		Identifier = identifier;
	}
	
}
